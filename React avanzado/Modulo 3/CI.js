// Integración Continua (CI) es una técnica de desarrollo de software en la que los desarrolladores utilizan un sistema de control de versiones, como Git donde los desarrolladores diariamente hacen sus contribuciones al desarrollo

// Cada cambio subido desencadena un conjunto de funciones que prueban y testean automaticamente el codigo, si alguno arroja un error, el CI se detiene hasta solucionarlo

// Al tener pequeños cambios controlados es mas sencillo solucionar los errores, es un flujo de trabajo muy comun en organizaciones, seria asi:
// Se crea la rama de codigo en GitHub
// Cuando el desarrollador envia su codigo el CI lo emula y hace las pruebas automatizadas
// En caso de salir mal, el desarrollador que lo subio le llegara un correo y el CI se pondra en rojo
// De lo contrario si sale bien, estara en verde el software pasara a la siguiente etapa

