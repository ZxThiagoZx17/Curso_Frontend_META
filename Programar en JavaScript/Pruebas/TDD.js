// TDD Test-Driven Development (Desarrollo guiado por pruebas)
// Es una metodologia de desarrollo de Software donde las pruebas se crean antes del codigo fuente

// La idea es saber como se quiere que funcione el programa antes de codificarlo, sigue estos 3 pasos:
// Red (Escribir una prueba que falle)
// Green (Escribir una prueba que pase)
// Refactor (Mejorar el codigo que se hizo, limpieza, optimizacion, legibilidad)